void printUInt8Table() {
    for (auto i = 0; i < 256; i++) {
        auto fp = static_cast<float>(i) / std::numeric_limits<uint8_t>::max();
        
        auto fpSRGB = fromLinearToSRGB(fp);
        auto fpLinear = fromSRGBToLinear(fp);
        auto srgb = static_cast<uint8_t>(std::min(255.0f, fpSRGB * std::numeric_limits<uint8_t>::max()));
        auto linear = static_cast<uint8_t>(std::min(255.0f, fpLinear * std::numeric_limits<uint8_t>::max()));
        
        printf("    { %d, %d, %a, %a },\n", srgb, linear, fpSRGB, fpLinear);
    }
}
